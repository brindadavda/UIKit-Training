import UIKit

extension Int {
    
    var int64:Int64 {
        return Int64(self)
    }
    
    var string:String {
        return String(self)
    }
    
    var double:Double {
        return Double(self)
    }
    
    var cgFloat:CGFloat {
        return CGFloat(self)
    }
}
